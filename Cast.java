class Cast
{
	public static void main(String args[])
	{	
		int i=123,j;
		byte b;
		double d=314.65;
		b=(byte)i;
		System.out.println("value of i : "+i);
		System.out.println("after conversion value of b : "+b);
		j=(int)d;
		System.out.println("value of d : "+d);
		System.out.println("after conversion value of j : "+j);
	}
}